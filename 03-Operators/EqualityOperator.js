
/**
 * Strict Equality ( Type + Value
 */

// Return True
console.log(1 === 1);

// Return False
console.log('1' === 1);


/**
 * Lose Equality
 */

// Return True
console.log(1 == 1);

// Return True. Automatically convert to number
console.log('1' == 1);

// Return True. Convert number to boolean
console.log(true == 1);