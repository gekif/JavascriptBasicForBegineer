
// Declare x
let x = 1;


/**
 * Relational Operator
  */

// Check if x more than 0. Return true
console.log(x > 0);

// x greater than
console.log(x >= 1);

// x less than
console.log(x < 1);

// x less than similar
console.log(x <= 1);


/**
 * Equality Operator
 */

// Check if x is very true
console.log(x === 1);

// Check if x is not very true
console.log(x !== 1);


